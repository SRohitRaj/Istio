// DeepCopyInto is an autogenerated deepcopy function, copying the receiver, writing into out. in must be non-nil.
func (m *MDPConfig) DeepCopyInto(out proto.Message) {
	bytes, err := json.Marshal(m)
	if err != nil {
		log.Print(err.Error())
		return
	}
	if err = json.Unmarshal(bytes, out); err != nil {
		log.Print(err.Error())
	}
}

// DeepCopy is an autogenerated deepcopy function, copying the receiver, creating a new MDPConfig.
func (m *MDPConfig) DeepCopy() *MDPConfig {
	if m == nil {
		return nil
	}
	out := new(MDPConfig)
	m.DeepCopyInto(out)
	return out
}

// DeepCopyObject is an autogenerated deepcopy function, copying the receiver, creating a new runtime.Object.
func (m *MDPConfig) DeepCopyObject() runtime.Object {
	if c := m.DeepCopy(); c != nil {
		return c
	}
	return nil
}

